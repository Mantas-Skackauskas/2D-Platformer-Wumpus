public class Arrow {

    private int arrowX;
    private int arrowY;

    public Arrow(){}

    public Arrow(int arrowX, int arrowY){
        this.arrowX = arrowX;
        this.arrowY = arrowY;
    }

    public int getArrowX() {
        return arrowX;
    }

    public int getArrowY() {
        return arrowY;
    }
}
