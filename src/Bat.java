public class Bat {
    private int batX;
    private int batY;

    public Bat(){}

    public Bat(int batX, int batY){
        this.batX = batX;
        this.batY = batY;
    }

    public int getBatX() {
        return batX;
    }

    public int getBatY() {
        return batY;
    }
}
